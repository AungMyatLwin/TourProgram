New Year's Day was originally observed on 15th March in the old Roman Calendar.<br>

It was fixed at 1st January in 153 BCE, by two Roman consuls. The month was named 
Janus after the name of the Roman god of doors and gates. Janus had two faces, 
one facing forward and one looking back, a fitting name for the month at the start of the year.<br>

During the Middle Ages, a number of different Christian feast dates were used
 to mark the New Year, though calendars often continued to display the months 
 in columns running from January to December in the Roman fashion.<br>

    It wasn't until 1582 when the Roman Catholic Church officially
	adopted 1 January as the New Year.<br>

Most countries in Western Europe had officially adopted 1st January 
