	<h2>When is Full Moon Day of Tazaungmone?</h2>
	This public holiday in Myanmar takes place on the full moon day of Tazaungmone, the eighth month in the Burmese calendar. It usually falls in November in the Western calendar.<br>
	Depending on the day of the week that the full moon falls on, extra public holidays may be declared on the other days of the festival.<br>
	
	<h3>History of Full Moon Day of Tazaungmone</h3>
	The full moon of Tazaungmone marks the end of the rainy season in Myanmar as well as the end of the Kathina festival during which monks are offered alms including new robes.<br>
	The number nine is a lucky and auspicious number in Myanmar and 9,999 candles are lit up as offerings to Buddha in the Chaukhtatgyi Paya Pagoda on the night of the full moon day of Tazaungmone. <br>
	Following on from Thadingyut, celebrated on the previous full moon, Tazaungmone is another festival of lights as the festival of Tazaungdaing is also held at this time. <br>
	Across Myanmar, hot-air balloons are lit with candles and released into the night sky. The balloons are released as an offering to the home of the devas in Buddhist cosmology and as a way to drive
	away evil spirits. <br>
	The Tazaungdaing festival in Taunggyi is one of the most spectacular in Myanmar when giant paper balloons are lit and sent up into the sky.<br>
	
	
