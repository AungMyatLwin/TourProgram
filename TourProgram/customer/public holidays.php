	<h2>When is Thingyan ?</h2>
	
	Thingyan is an public holiday in Myanmar usually celebrated over three days beginning on 13 April.
	The public holidays given for each year will depend on which day of the week, 13 April falls on.
	
	Thingyan culminates in the Burmese New Year and the period of holidays are the most important and 
	the biggest festival in Myanmar.
	
	Formerly the dates of Thingyan would have been based on the Burma lunisolar calendar, 
	but the dates have now been fixed on 13 April.<br>
	
	<h3>What are the traditions of Thingyan ?</h3>
	
	Thingyan is is celebrated at the same time as many other New Years in the region. 
	Thingyan historically marked the changing of the year by the sun moving from Pieces to Aries.
	The word Thingyan is thought to have derived from ancient Sanskrit and means 'change'.<br>
	
	This is an ancient festival and when it was first observed, the movement of the sun into Aries 
	would have been closer to the Vernal Equinox (21 March) which marks the arrival of spring in 
	the northern hemisphere. The dates of these events differ today due to the wobble of the earth on
	its axis over a 25,000 cycle - known as procession.<br>
	
	The legend of the origin of Thingyan relates to a argument over a mathematical problem between two gods.
	The loser of this argument had his head cut off. However, when his head hit the ground, the earth would burn;
	if thrown in the sea, the water would dry up and if thrown in the air, it would explode in a fireball.
	To keep this from happening, seven goddesses took turns holding the head for a year each, changing over on Thingyan.
	The winner of the argument cut off the head of an elephant and stuck it on the headless body, thereby creating the god,
	Ganesh.<br>
	
	<h4>How is Thingyan celebrated ?</h4>
	
	Even though Thingyan has now been fixed in mid-April, its current date is a welcome position in the calendar as it occurs
	at the hottest time of the year in Myanmar and being a water festival, it is a great opportunity to cool down and get some 
	relief from the sweltering heat.<br>
	
	If you are in Myanmar during Thingyan, prepare to get wet! Everyone is likely to get soaking as people roam around with
	buckets of water or hose, spraying water on anyone they can. This tradition originates from the custom of washing away 
	the bad luck and misfortune of the previous year, entering the new year cleansed from the past. Water cleansing is
	a custom common in many New Year festivals around the world.<br>
	
	In the 13th century, chronicles from the time that King Narathihapate, the last ruler of the Bagan Dynasty,
	built corridors that ran from his palace to the bank of the Ayeyarwaddy River and that during Thingyan 
	they were used for water throwing.<br>
	</p>
	
