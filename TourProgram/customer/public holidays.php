	<h2>When is Full Moon Day of Waso ?</h2>
	
	Also known as Dhammasetkya Day, this public holiday in Myanmar takes place on the full moon day of Waso, 
	the fourth month in the Burmese calendar. It usually falls in July in the Western calendar.<br>
	Depending on the day of the week that the full moon falls on, extra public holidays may be 
	declared on the other days of the festival.<br>
	
	<h3>History of Full Moon Day of Waso</h3>
	
	This day, the first full moon of the fourth month in the Burmese calendar, commemorates the Buddha’s first
	sermon to five monks in the Deer Park in Benares, India and the founding of the Buddhist sangha (monkhood)
	about 2,500 years ago.<br>
	In the sermon, which is known as 'Setting the Wheel of Dhamma in Motion', the Buddha first spelled out the 
	Four Noble Truths and the Noble Eightfold Path.<br>
	Waso Full Moon Day marks the beginning of Buddhist lent (Wasso), which generally coincides with the height 
	of Myanmar’s rainy season. Buddhist Lent last for three months from the full moon of Waso until the full moon of 
	Thadingyut. It is normally a time of heightened religious activity around the country. Pilgrims throughout the country
	gather wild flowers to offer at pagodas, and offer new robes to monks. Almost all monks will not leave the grounds of their 
	monastery during this time.<br>
	During the period of Buddhist Lent, many Buddhists will abstain from eating meat, drinking alcohol or smoking. It is also
	traditionally a time when Myanmar people are not allowed to move house.<br>
	
	
