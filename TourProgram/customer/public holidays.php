	<h2>When is Full Moon Day of Thadingyut ?</h2>
	This public holiday in Myanmar takes place on the full moon day of Thadingyut, the seventh month in the Burmese calendar.
	It usually falls in October in the Western calendar.<br>
	The Thadingyut Festival lasts for three days from the day before the full moon through to the day after the full moon. <br>
	Depending on the day of the week that the full moon falls on, extra public holidays may be declared on the other days of the festival.<br>
	The festival marks Buddha's preaching of Abhidhamma to the gods in Trayastrimsa and the end of the Buddhist period of fasting.<br>
	
	<h3>History of Full Moon Day of Thadingyut</h3>
	
	Known as the Festival of Lights, Thadingyut is the second most popular festival in Myanmar after Thingyan Festival.<br>
	Maya, the mother of Buddha died a week after Buddha was born. She was reborn in the Trayastrimsa Heaven as a god named Santusita.
	To honour his mother, Buddha ascended to the Trayastrimsa Heaven and preached from the Abhidhamma texts to Santusita for three months.<br>
	The full moon of the month Waso (Dhammasetkya Day) marks the ascent by Buddha and the start of the three month period of Buddhist Lent, when the monks 
	retreat to their monasteries. During this time, monks dedicate themselves to meditation and study. During Buddhist Lent, marriages are forbidden and many people
	give up meat and alcohol. Buddhist lent often coincides with the rainy season in Myanmar.<br>
	The full moon in Thadingyut marks Buddha's return to earth and signifies the end of Buddhist Lent.<br>
	Thadingyut is called the festival of lights as the followers of Buddha lit up their houses and temples to mark the return of Buddha. Towns and villages across Myanmar
	will be illuminated in honour of this auspicious event. <br>
