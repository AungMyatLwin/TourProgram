    <h2>When is Maka Bucha?</h2>
	
	Maka Bucha is a public holiday in several Mekong region countries such as Cambodia and Thailand. <br>
	
	The date of this important Buddhist festival depends on Lunar cycle. The holiday may also be known as Magha Puja. <br>
	
	On this day, the full moon of the third lunar month (called Tabodwe); seven months after Buddha began his teachings,
	over a thousand monks gathered to hear Buddha preach. 
	
	Buddha ordained these monks and spread the principles of Buddhism. This marked a key event in the development 
	of the religion.
	
	45 years later, on the same full moon in the third lunar month, Buddha again delivered his teachings shortly
	before his death.
	
	This third lunar month on which both events occurred is known in Buddhist Pali language as 'Makha'. 'Bucha',
	means to honour.<br>
	
	<h3>How is Maka Bucha Day celebrated?</h3>
	
	
	In the morning many Thai people wake up early to give alms to monks. In the evening, temples are
	full of people listening to sermons. They often perform a ritual known as the the candle ceremony 
	where they walk clockwise three times around the temple. holding flowers, incense and a lighted candle.
	Each of the three circuits represents one of the three jewels (ideas at the heart of Buddhism) - Buddha,
	Dharma and Sangha.<br>
	
	Myanmar<br>
	
	In Myanmar, Maka Bucha is known as the Full Moon Day of Tabaung. Tabaung is the last month of the year
	in the Burmese calendar year.The holiday is marked by pagoda festivals with the largest taking place
	at the Shwedagon Pagoda in Yangdon.<br>
