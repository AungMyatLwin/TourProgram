In the afternoon, after a visit to lacquer ware workshops; continue to Manuha Temple built by exile King Manuha; Nanphaya, said to have been the residence of King Manuha; Ananda Temple, architectural masterpiece and Oak-Kyaung monastery decorated with multi-color mural paintings. Enjoy the breathtaking sunset from one of the temple of Bagan. Overnight at the hotel in Bagan.
