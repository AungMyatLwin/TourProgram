              <p class="p1"><b>
<h1>PASSPORT AND VISA</h1><br>
<p><h3>Visitors must have a valid passport with an EVT [Entry Visa for Tourist: </h3>Package visa and FIT visa] to enter Myanmar. EVT can be obtained at any Myanmar Embassy / Consulate offices with validity for 28 days. Visa-on-Arrival can be arranged for those countries where Myanmar Embassy / Consulate office does not exist.</p>

<br>
<p>ENDORSING THE VISA ON ARRIVAL AT THE YANGON AND MANDALAY INTERNATIONAL AIRPORT IS SUSPENDED *</p><br>

<p>
<h3>Pre-Arranged Visa on Arrival</h3>

(Only for those who live in the countries where no Myanmar Embassy Exits)

Travelers who request Pre-Arranged Visa on Arrival would follow below practice.


• The Service is only for travelers who buy package tour. (Not for FIT)

• Travelers must stay and visit as per purchased package tour. Not allowed any furthermore activities.

• Stay extension is not permitted.
</p>
<p>
Visa On Arrival

For a pre-arranged visa on arrival, please provide us the below documents; <br>
<ol>
<li> Full names in your passport </li>
<li>  Passport No. </li>
<li>  Father’s Name </li>
<li>  Date of Birth & Place of Birth </li>
<li>  Place of Issuance / Expiry date, </li>
<li>  Nationality & Sex </li>
<li>  Date of arrival & Flight in/out no. </li>
<li>  Current Address </li>
<li>  Occupation </li>
<li>  Vision of visit to Myanmar </li>
<li>  Religion </li>
<li> Photo copy send by email to be required</li>
</ol><br>
</p>
<p>
<h3>CURRENCY EXCHANGE</h3><br>

The currency in Myanmar is called <b style="font-weight: bolder; font-size: 100%; font-style: oblique; font-family: 'Open Sans', sans-serif; color: #052290;"> Kyat</b> (pronounced 'chat'). As in many countries of the area the US Dollar is the most useful currency to carry and it can be exchanged into local currency.<br>

However Traveler’s Cheque and International credit cards are not widely used. Traveler’s cheques can currently NOT be used or exchanged in Myanmar. It is absolutely necessary to bring enough cash in USD or EURO. Other foreign currencies are difficult to change. There is not anymore required to change 200 US Dollars into 200 FEC (Foreign Exchange Certificates) upon arrival at the airport.</p>
<p>
<h3>HEALTH</h3>
No vaccination certificate is required unless coming from the infected area.</p>
<p>
<h3>ELECTRICITY</h3>
Myanmar uses 220-250V, 50Hz. power-cuts are quite common but most hotels have their own generator.</p>
<p>
<h3>CULTURE</h3>
Myanmar lies between two great civilizations, India and China, but it has developed its own distinctive culture. Buddhism has a great influence in the daily lives of the Myanmar people. The people have preserved the traditions of close family ties, respect for elders and simple native dress. While tolerance and contentment are the characteristics of the people, Myanmar hospitality is legendary.</p>
<p>
<ul>
<p>Myanmar is a country deeply respectful of their religious and cultural traditions. Visitors will not cause offense if they note a few things:</p>

<li>Remove shoes and socks before entering religious buildings and compounds. Some monasteries allow food-wear in the compound but not inside the buildings, starting with lowest step! Better make sure first. It is also polite to remove shoes before entering a private home but socks may be left on.</li><br>

<li>
When handing someone money or a gift use the right hand or both hands with the exception of paying the bill in a teashop or restaurant. Then it is a more casual transaction.</li>
</ul>
</p><br>
<p>
<h3>RELIGION</h3>
Everyone in Myanmar has their independent right in religion. Theravada Buddhism is the predominant religion with over 80% of the population professing it. There are also Christians, Muslims and Hindus.</p>
<p>
<h3>CLOTHING</h3>
Visitors are required to dress decently on the precincts of religious buildings. Ladies should not wear shorts, briefs or bra-less T-shirt on Shwedagon Pagoda. Shoes and socks or stockings must be removed at Pagodas and Monasteries.</p>
<p>
<h3>CUSTOM’S FORMALITIES ON ARRIVAL</h3>
All foreign currencies in excess of US$-2,000, Traveler’s Cheque and jewelry, cameras and electronic goods etc, must be recorded on the customs form which may be checked on departure.</p>
<p>
<h3>SHOPPING</h3>
You cannot post parcels home, although some shops will do the shipping for you. Ask first before you buy anything bulky. You cannot take back antiques. Better to avoid buying old-looking Buddha images even if they were made a week ago, as the Custom Officers may not allow its export. Buy jewellery only from authorized dealers and be sure to get a receipt which much be shown at Customs checkpoint at the airport.
</p>
<p>
<h3>COMFORT & CARE</h3>
Mosquito repellent and sunscreen are highly recommended, especially when travelling to remote areas in conjunction with other measure to prevent mosquito bites. There are no compulsory vaccinations for travel to Myanmar (Burma) but it is advisable to take precautions against malaria. Don’t forget to bring medication you would normally use in emergencies, such as antibiotics etc. Most medicines are available but maybe not the brand you normally use.</p>
<p>
Try to avoid going out in the midday sun when it’s very hot. Drink only pre-boiled water such as green tea or bottled water of reputable brands. Better to avoid ice cubes in the streets stalls.

Avoid eating shellfish in hot weather, if you are not actually on the beach.
</p>
<p>
<h3>FOOD</h3>
Basic Myanmar food is mainly rice and curry. Myanmar curries tend to be less spicy than those served in Thailand. Meals & drinks at International Hotels are considerably expensive. Myanmar is also a country of seafood. Crabs, prawns, lobsters and other shellfish are maong the popular dishes avaliable at the most Myanmar and Chinese restaurants in Yangon and other tourist destination.

There are many good local restaurants with almost the same quality of food and hygiene as hotels but at reasonable prices. They serve various dishes of Myanmar, Chinese, European (French and Italian), Thai and Indian cuisine. But roadside small restaurants are not recommended, as it can be risky for health.

There are also a wide variety of local snacks and delicacies. Mouk-hin-kha (thin rice noodles served with fish gravy and onion soup) and Ohn-noh-khauk-swe (noodles served with chicken and coconut gravy) are the most popular in Myanmar. Shan food is another delicacy. Shan style of cooking neither belongs to Myanmar nor Chinese nor Thai cuisine but they taste really good in their own way.

Restaurants and food served towards the travelers are carefully selected for hygiene as well as gastronomic considerations.

Myanmar has a variety of vegetables and fruits, both tropical citrus all year round or seasonally. The most common ones are mango, banana, durian, jackfruit, avocado, grapefruits, grapes, mangosteen, papaya, pomelo, pineapple, watermelon, orange and Washinton navel.<br>
</p>
