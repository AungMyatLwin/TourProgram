                <h3>Culture</h3> 
                <p>Myanmar's culture is heavily influenced by Buddhism. Influence of ancient Indian culture intertwined with local traditions and some Chinese influences can be clearly seen in local architecture and food habits. Various stupas and temples throughout the country bear a distinct resemblance to those in northern India. Like neighbouring Thailand, Theravada Buddhism is the single largest religion, and even some of the most remote villages will have a village temple for people to pray. Other religions which exist in smaller numbers include Christianity, Islam and Hinduism.</p>           
