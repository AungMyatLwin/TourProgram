              <h3>History</h3>

				<p>
                <p>Myanmar's people and history is a glorious mishmash of settlers and invaders from all fronts. The Mon and the Pyu are thought to have come from India, while the now dominant Bamar migrated through Tibet and, by 849, had founded a powerful kingdom centred on Bagan. For the next millennium, the Burmese empire grew through conquests of Thailand (Ayutthaya) and India (Manipur), and shrank under attacks from China and internal rebellions.</p>
<p>Eventually, Britain conquered Burma over a period of 62 years (1824-1886) and incorporated it into British colonial Empire. It was administered as a province of India until 1937 when it became a separate self-governing colony. Most of the jobs in the British led administration were occupied by people brought from India and the local Bamar people were sidelined.The local farmers became preys of south Indian money lenders who confiscated their land. The oppression of British rule prompted the Burmese to co-operate with the Japanese during the Second World War. Burma was a major battleground as the Allies fought the Japanese for dominance over Asia.</p>
<p>The Burma Road was built to get supplies to China. The Thailand-Burma railroad (the so-called "Death Railway") from Kanchanaburi in Thailand over the River Kwai to Burma was built by the Japanese using forced labor — a great number of people (estimated at 80,000) died during construction of the railway. Large parts of Western Burma, particularly the hilly areas bordering India and the city of Mandalay were severely damaged during the war.</p>
<p>While the Burmese independence fighters led by Aung San initially cooperated with the Japanese to oust the British, with the Japanese promising to grant independence to Burma in exchange, it soon became apparent that the Japanese promises of independence were empty. The Japanese occupation was very brutal, and many Burmese were killed, such as in the Kalagong massacre. Aung San subsequently switched allegiance and helped the British win Burma back from the Japanese. Aung San subsequently led negotiations with the British for Burmese independence after the end of World War II, and the British agreed in 1947 to grant independence to Burma the following year, though Aung San himself was assassinated later in the year and never lived to see his dream come true. Independence from the British under the name Union of Burma was finally attained in 1948, and till this day, Aung San is regarded by most Burmese people to be the father of independence.</p>
<p>The new union brought together various states defined by ethnic identity, many of whom had centuries-long histories of autonomy from and struggles against each other. In the interest of securing their collective independence from Britain, the tribes reached an agreement to submit to collective governance -- with power sharing among the ethnicities and states -- for ten years, after which each tribe would be afforded the right to secede from the union. The terms of this "Panglong Agreement" were enshrined in the 1947/1948 constitution of the new Union of Burma. The new central government of the nation quickly worked to consolidate its power, marginalizing and angering tribal leaders and setting off more than a decade of armed conflict. In 1961, more than 200 ethnic leaders from the Shan, Kachin, Red Karen, Karen, Chin, Mon and Rakhine people met with ethnic Bamar (Burmese) central government authorities to draft a new form of government which would ensure the tribes both autonomy and self-determination within a federal system.</p>
<p>General elections were held in 1960 and U NU took over as prime minister. In 1962 General Ne Win led a coup d'état which ousted the first democratically elected government in Burma, and subsequently installed himself as leader. General Ne Win dominated the government from 1962 to 1988, first as military ruler, then as self-appointed president, and later as political kingpin. Pro-democracy demonstrations in 1988 were violently crushed, with general Saw Maung taking over in a coup and installing the State Law and Order Restoration Council (SLORC) to rule the country, now renamed Myanmar.</p>
<p>Multiparty legislative elections were held in 1990, with the main opposition party - the National League for Democracy (NLD) - winning a landslide victory (392 of 489 seats). But SLORC refused to hand over power, instead placing NLD leader and Nobel Peace Prize recipient Aung San Suu Kyi under house arrest, which she has endured for 14 of the last 20 years.</p>
<p>What was once one of the richest and most developed countries in Asia has since slumped into poverty due to inefficient economic policies, rural poverty and corruption. The junta took steps in the early 1990s to liberalize price controls after decades of failure under the "Burmese Way to Socialism," but had to reinstate subsidized prices on staples in the face of food riots, upon which the democracy movement grafted its agenda. The government called out troops and the rioters were defiant until the monks intervened: standing between both sides, they told everyone to go home and they did. The riots caused overseas development assistance to cease and the government subsequently nullified the results of the 1990 legislative elections.

In response to the government's attack in May 2003 on Aung San Suu Kyi and her convoy, the USA imposed new economic sanctions against Myanmar, including bans on imports of products from Myanmar and on provision of financial services by US citizens.</p>
<p>The summer of 2007 was marked by demonstrations against the military government which were again brutally suppressed. The demonstrations started in August, apparently in an uncoordinated manner, as a protest against a stiff hike in the price of petrol, but morphed into a more serious challenge to the government after three monks were beaten at a protest march in the town of Pakokku. The monks demanded an apology but none was forthcoming and soon processions of monks with alms bowls held upside down filled many cities (including Sittwe, Mandalay, and Yangon). Yangon, particularly the area around Sule Pagoda in the downtown area, became the centre of these protests. While the monks marched, and many ordinary citizens came out in support of the monks, the world watched as pictures, videos, and blogs flooded the Internet. However, the government soon suppressed the protests by firing on crowds, arresting monks and closing monasteries, and temporarily shut down Internet communications with the rest of the world. This led the USA, Australia, Canada and the European Union to impose additional sanctions, some targeting the families and finances of the military leaders. Dialogue between the UN and the military government has stalled.

Despite international condemnation, Aung San Suu Kyi was back under house arrest after being charged of breaching the conditions of her house arrest. She was released from house arrest on 13 November 2010. As of November 2011 Aung San Suu Kyi is participating in politics and the prospects for democracy look better than ever.</p>
                
