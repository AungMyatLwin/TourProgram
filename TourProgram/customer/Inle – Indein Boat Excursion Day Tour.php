                <div class="a1"><div class="content-box"><p align="justify">This morning, we will board a local motor boat to start our exploration of 22-Km-long Inle Lake, which is inhabited by many different tribes. We pass stilted villages, the leg-rowing Intha fishermen and floating gardens built up from strips of water hyacinth and mud anchored to the lake’s bottom with bamboo poles. We continue to PhaungDawOo Pagoda, the lake’s main sanctuary, which houses 5 images of Buddha covered in gold leaf, Nampan village where locals make cigars and boats. We also see NgaHpeChaung, also known as ‘Jumping Cat Monastery’ and famous for its collection of antique Buddha statues. Continue our excursion by motorized boat to Indein pagoda complex, in the south-west of the lake. The boat leaves the lake and enters a narrow canal with a beautiful natural environment to continue to a small Pa O tribe village. From the village, follow an old covered path to a 13th century monastery and the very impressive ruins of hundreds of ancient pagodas, some hidden in the vegetation. The main stupa-area on the top of the hill offers breathtaking views over the surrounding mountains and down to the valley and the lake in the far distance. Transfer back to hotel.</p></div></div>
