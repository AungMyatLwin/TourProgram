<p style="text-align: justify;">Breakfast at Hotel. Transfer to airport for Mandalay. Visit Mahagandaryone Monastery For offering alm food to Thousand monks, U Pain Wooden Bridge, Mahamuni Image - a bronze Buddha image castled during the lifetime of Buddha himself, Tapestry works, Wood carving &amp; bronze castling workshops, Marble carving workshop and silk weaving home cottage. Lunch at restaurant. After lunch, continues to Mandalay Palace, Shwenandaw Monastery - noted for its exquisite wood carvings, gold leaf beating workshop, Atumashi Monastery, Kuthodaw Pagoda - known as the world biggest book and finish the tour by visiting Mandalay Hill to watch the sun-set and the panoramic view of the city. Dinner at restaurant.<br>Overnight at Hotel.</p>
