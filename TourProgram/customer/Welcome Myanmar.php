<p style="text-align: justify;">Breakfast at Hotel. Transfer to airport for Bagan. Sightseeing in Bagan by visiting: Nyaung Oo Market, Shwezigon Pagoda - the prototype for later Myanmar pagodas built by King Anawrahta, Ananda Temple - King Kyansittha's masterpiece and the crowing achievement of the early style temple of architecture, Kyansitha Umin, Thatbyinnyu Temple - the highest building on the Bagan plain and Htilominlo Temple - the largest temples of Bagan. Lunch at restaurant. Evening visit Lacquer-ware making home cottage, Manuha Temple, Myinkaba Gubyaukgyi Temple - known for its wall paintings depicting scenes from the Jataka and Dhamanyangyi Temple. Evening sun-set view of Bagan from Buphaya or Shwesandaw Pagoda. Dinner at restaurant with Puppet Show.<br>Overnight at Hotel.</p>
